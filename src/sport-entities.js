
/**
 * The most common type of score, when two opponents face each other and each of them can have a scored number of something
 */
class DuelScore {

    /**     
     * @param {ContenderScore} contender1 the scores of the first contender
     * @param {ContenderScore} contender2 the scores of the second contender
     * @param {draw}
     */
    constructor(contender1, contender2) {
        this.contender1 = contender1;
        this.contender2 = contender2;
    }
}

/**
 * All scores of one contender in a specific context
 */
class ContenderScore {

    /**
     * 
     * @param {String} contenderId some identification by contender but not necessarily an database autogenerated id
     * @param {Number} value the number of scores
     */
    constructor(contenderId, value) {
        this.contenderId = contenderId;
        this.value = value;
    }
}

module.exports = {
    DuelScore,
    ContenderScore
}